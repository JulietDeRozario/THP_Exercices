            LE SOLAIRE:

    matériaux utilisés pour sa construction :

    -silicium (sable) -> l’électricité acheminée jusqu’aux fours de silicium 
     et usines de production de cellules solaires est exclusivement produite grâce
     aux centrales électriques au charbon, construites autour de la ville.
     40 kilos de charbon étaient nécessaires en Chine pour produire un panneau de 1 m sur 1,5 m,
     dont la durée de vie est estimée à 20 ans. Alors que la même quantité de charbon brûlée en centrale
     fournirait assez d’électricité pour alimenter une ampoule de 22 W, 12 heures par jour pendant 30 ans ! 
     L’étape primordiale reste le raffinage du silicium, préalablement fondu en four. Plus le silicium sera purifié,
     plus les capacités d’absorption de la lumière du soleil par la cellule seront optimales. Sauf que ce sont les
     bains d’acide qui permettent cette purification. L’opération chimique rejette du tétrachlorure de silicium
     et du chlore, dont le recyclage est encore très onéreux. 
    

    
